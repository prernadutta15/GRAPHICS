    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(flag!=1){
        try{
        float m;
        float n;
        float ax=(float)Integer.parseInt(af.getText());
        float bx=(float)Integer.parseInt(bf.getText());
        if(ax==0){
            m=0;
            n=0;
        }
        else if(ax==1&&bx==1){
            m=1/(float)Math.sqrt(2);
            n=1;
        }
        else{
        m=(ax/bx)/(float)Math.sqrt(2);
        n=(ax/bx)/(float)Math.sqrt(2-(ax*ax)/(bx*bx));
        }
        float th=(float)Math.asin(m);
        float ph=(float)Math.asin(n);
        System.out.println(th+"  "+ph);
        flag=1;
        Graphics2D gfx=(Graphics2D)Panel.getGraphics();
        ncube=Cube.rotateCubeYradian(cube.points,ph);
        ncube=Cube.rotateCubeXradian(ncube.points,th);
        ncube=Cube.ProjectZ(ncube.points);
        ncube=Cube.ScaleUp(ncube.points, 100);
        ncube=Cube.MirrorY(ncube.points);
        ncube=Cube.translate(ncube.points, 0,400);
        ncube.drawCube(gfx, Color.red);
        }catch(Exception e){
           JOptionPane.showMessageDialog(fram,"Please Enter theta and Phi"); 
        }
        }else{
            JOptionPane.showMessageDialog(fram,"Please Clear Screen First."); 
        }
    }//GEN-LAST:event_jButton1ActionPerformed

